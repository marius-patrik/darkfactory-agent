"""Hand-authored sys.path bootstrap for the Rommie/Andromeda Python stubs [S3.0].

THIS FILE IS NOT GENERATED BY buf/protoc.  It is a hand-authored shim that
must be preserved across every ``buf generate proto`` run.  Only the sibling
``*_pb2.py`` / ``*_pb2.pyi`` files are regenerated by buf.

Why this file exists
--------------------
The protoc-generated ``*_pb2`` modules use absolute imports rooted at the
proto package (``from rommie.v1 import common_pb2``). This package dir is the
root of that ``rommie/`` tree, so importing it inserts the tree into
``sys.path`` and makes ``rommie.v1.*`` importable.

Usage::

    import agent.gen  # registers the gen root on sys.path
    from rommie.v1 import session_frames_pb2, registry_pb2

Regeneration notes (see docs/contracts/wire.md §5)
---------------------------------------------------
- Run ``buf generate proto`` from the repo root to refresh the ``*_pb2``
  stubs after any ``.proto`` edit.
- Do NOT wipe / ``--clean`` this directory before regenerating — buf does not
  emit ``__init__.py`` files, so cleaning would destroy this bootstrap and
  the ``rommie/`` and ``rommie/v1/`` package markers, breaking
  ``import agent.gen``.
- DO NOT EDIT the ``*_pb2.py`` / ``*_pb2.pyi`` files by hand.
"""

import os as _os
import sys as _sys

# Make the generated ``rommie/`` package tree importable as a top-level package
# (the generated code does ``from rommie.v1 import ...``).
_gen_root = _os.path.dirname(__file__)
if _gen_root not in _sys.path:
    _sys.path.insert(0, _gen_root)
